package module;

public class TituloGrupo {
	private String titulo;
	private String grupo;

	public TituloGrupo(String titulo, String grupo) {
		this.titulo = titulo;
		this.grupo = grupo;

	}
	
	public String getTitulo() {
		return this.titulo;
	}
	
	public String getGrupo() {
		return this.grupo;
	}
	
}
